// Function declaration
function add(a, b) {
    return a + b;
}
console.log(add(2, 3)); // Output: 5

// Function expression
const multiply = function(a, b) {
    return a * b;
};
console.log(multiply(2, 3)); // Output: 6

// Arrow function
const subtract = (a, b) => a - b;
console.log(subtract(5, 3)); // Output: 2