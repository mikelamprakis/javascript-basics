// If-else statement
let num = 10;
if (num > 5) {
    console.log("Number is greater than 5");
} else {
    console.log("Number is 5 or less");
}

// Switch statement
let day = 3;
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    default:
        console.log("Another day");
}

// Ternary operator
let isRaining = true;
let weather = isRaining ? "rainy" : "sunny";