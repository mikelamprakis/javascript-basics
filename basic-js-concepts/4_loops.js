// for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// for loop with an array
let fruits = ["apple", "banana", "cherry"];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

// for-of loop
let colors = ["red", "green", "blue"];
for (let color of colors) {
    console.log(color);
}

// while loop
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// do-while loop
let j = 0;
do {
    console.log(j);
    j++;
}while (j < 5);

